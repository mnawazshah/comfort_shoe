    private void nameTfKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameTfKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String name = nameTf.getText().toLowerCase();

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Must enter a name", "Invalid Entry", JOptionPane.ERROR_MESSAGE);

            } else if (!name.matches("^[a-z A-Z]*$")) {
                JOptionPane.showMessageDialog(null, "Name can not have digits or special characters", "Invalid Entry", JOptionPane.ERROR_MESSAGE);

            } else {

            }

        }
    }//GEN-LAST:event_nameTfKeyPressed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed

        int id = Integer.valueOf(idTf.getText());
        String name = nameTf.getText();
        String supplierName = suppliersCb.getSelectedItem().toString();

        if (supplierName.equals("Select Company Name")) {

            JOptionPane.showMessageDialog(this, "Please select a company name", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                st = conn.prepareStatement("insert into brands values(?,?,?)");
                st.setInt(1, id);
                st.setString(2, name);
                st.setString(3, supplierName);
                st.executeUpdate();

                JOptionPane.showMessageDialog(this, "Brand Inserted Sucessfuly", "Success", JOptionPane.INFORMATION_MESSAGE);
                Refresh();
                brandsTable.setRowSelectionInterval(id - 1, id - 1);
                brandsTable.scrollRectToVisible(brandsTable.getCellRect(id - 1, 0, true));
                getID();

                this.dispose();
                new BrandsForm().setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(AddBrand.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_addBtnActionPerformed
    int id = 0;

    void getID() {

        try {
            st = conn.prepareStatement("select max(id) from brands");
            rs = st.executeQuery();
            if (rs.next()) {
                id = rs.getInt(1) + 1;
                idTf.setText(id + "");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void getSuppliers() {
        try {
            conn = DBConnection.getConnection();
            st = conn.prepareStatement("select name from supplier_contact_details");
            rs = st.executeQuery();

            while (rs.next()) {
                String name = rs.getString(1);
                suppliersCb.addItem(name);

            }

        } catch (SQLException ex) {
            Logger.getLogger(AddBrand.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void getAllBrands() {
        try {
            conn = DBConnection.getConnection();

            st = conn.prepareStatement("select * from brands");
            rs = st.executeQuery();

            while (rs.next()) {
                int id = rs.getInt(1);
                String name = rs.getString(2);
                String supplier = rs.getString(3);

                Object[] obj = {id, supplier, name};
                DefaultTableModel tb = (DefaultTableModel) brandsTable.getModel();
                tb.addRow(obj);

            }
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeForm.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }

    void Refresh() {
        DefaultTableModel tb = (DefaultTableModel) brandsTable.getModel();
        tb.setRowCount(0);
        getAllBrands();
    }

