    void getID() {

        try {
            st = conn.prepareStatement("select max(id) from employee_signup");
            rs = st.executeQuery();
            if (rs.next()) {
                id = rs.getInt(1) + 1;
                idTf.setText(id + "");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void getAllEmployees() {
        try {
            conn = DBConnection.getConnection();

            st = conn.prepareStatement("select * from employee_signup order by id desc");
            rs = st.executeQuery();

            while (rs.next()) {
                int id = rs.getInt(1);
                String name = rs.getString(2);
                String email = rs.getString(3);
                String contact = rs.getString(4);
                String qualif = rs.getString(5);
                String role = rs.getString(6);
                int salary = rs.getInt(7);
                String username = rs.getString(10);
                String password = rs.getString(11);

                Object[] obj = {id, name, email, contact, qualif, role, salary, username, password};
                DefaultTableModel tb = (DefaultTableModel) employeeTable.getModel();
                tb.addRow(obj);

            }
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void Refresh() {
        DefaultTableModel tb = (DefaultTableModel) employeeTable.getModel();
        tb.setRowCount(0);
        getAllEmployees();
    }
