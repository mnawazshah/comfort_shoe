    private void passwordTfKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordTfKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String password = passwordTf.getText();

            if (password.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter a password", "Invalid Entry", JOptionPane.ERROR_MESSAGE);

            } else if (password.length() >= 8) {
                if (!password.matches(".*[A-Z].*")) {
                    JOptionPane.showMessageDialog(null, "Password must contain atleast one digit", "Invalid Entry", JOptionPane.ERROR_MESSAGE);

                } else if (!password.matches(".*[0-9].*")) {
                    JOptionPane.showMessageDialog(null, "Password must contain atleast one Upper Case alphabet", "Invalid Entry", JOptionPane.ERROR_MESSAGE);

                } else {
                    AddEmployee();

                }
            } else {
                JOptionPane.showMessageDialog(null, "Password must be greater than 8 letters", "Invalid Entry", JOptionPane.ERROR_MESSAGE);

            }

        }
    }//GEN-LAST:event_passwordTfKeyPressed

