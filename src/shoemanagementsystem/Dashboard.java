    void getEmployees() {
        try {
            st = conn.createStatement();
            rs = st.executeQuery("select count(*) from employee_signup");
            if (rs.next()) {
                int noOfEmployees = rs.getInt(1);
                empLbl.setText(noOfEmployees + "");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    void getProducts() {
        try {
            st = conn.createStatement();
            rs = st.executeQuery("select count(*) from stock");
            if (rs.next()) {
                int noOfProducts = rs.getInt(1);
                productsLbl.setText(noOfProducts + "");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    void getPurchases() {
        try {
            st = conn.createStatement();
            rs = st.executeQuery("SELECT  DATE_FORMAT(p_date, '%Y/%m/%d'),sum(total_cost) FROM   stock WHERE   p_date BETWEEN CURDATE() - INTERVAL 30 DAY AND CURDATE();");
            if (rs.next()) {
                int noOfProducts = rs.getInt(2);
                purchaseLbl.setText(noOfProducts + "");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

